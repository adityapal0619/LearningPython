#list
my_list=[1,2,3,4,5]
print(my_list)#print the list 
#list methods
print(my_list.index(1))#index method is used to find the index of an element in the list
print(my_list)#print the list after finding the index
print(my_list.append(6))#append method is used to add an element at the end of the list
print(my_list)#print the list after appending 
print(my_list.extend([7,8,9]))#extend method is used to add multiple elements at the end of the list
print(my_list)#print the list after extending
print(my_list.insert(0,12))#insert method is used to insert an element at a specific index
print(my_list)#print the list after inserting
print(my_list.remove(12))#remove method is used to remove an element from the list
print(my_list)#print the list after removing
print(my_list.pop())#pop method is used to remove the last element from the list
print(my_list)#print the list after popping
print(my_list.clear())#clear method is used to remove all the elements from the list
print(my_list)#print the list after clearing
my_list=[1,2,3,4,5]#reinitializing the list for further operations
print(my_list.count(1))#count method is used to count the number of occurrences of an element in the list
print(my_list)#print the list after counting
print(my_list.sort())#sort method is used to sort the list in ascending order
print(my_list)#print the list after sorting
print(my_list.reverse())#reverse method is used to reverse the list
print(my_list)#print the list after reversing
print(my_list.copy())#copy method is used to copy the list
print(my_list)#print the list after copying
 