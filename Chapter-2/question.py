# Program to input 2 numbers & print their sum

# Taking input from the user
int1 = int(input("Enter first number: "))
int2 = int(input("Enter second number: "))

# Calculating the sum
sum = int1 + int2

# Displaying the result
print(f"The sum of {int1} and {int2} is {sum}.")


# Program to input the side of a square & print its area

# Taking input for side of the square
side = int(input("\nEnter the side of the square: "))

# Calculating the area of the square
area = side * side

# Displaying the result
print(f"The area of the square with side {side} is {area}.")
